    {
       
          $user=new User();
          $user->name=$request->name;
          $user->address=$request->address;
          $user->email=$request->email;
          $user->phonenum=$request->number;
          $user->dob=$request->dob;
          $user->gender=$request->gender;
          $user->password='admin';
          $user->save();
          return redirect()-> to('Registration');

    }
    

